/* nonreentrant.c

   Демонстрация неатомарности (или ненадёжности) некоторых библиотечных функций, в
   данном примере — функции crypt(3).
*/
#if ! defined(_XOPEN_SOURCE) || _XOPEN_SOURCE < 600
#define _XOPEN_SOURCE 600
#endif
#include <unistd.h>
#include <signal.h>
#include <string.h>
#include <crypt.h>
#include "tlpi_hdr.h"

static char *str2;                  /* Устанавливается из argv[2] */
static volatile int handled = 0;    /* Подсчитывает количество вызовов обработчика */

static void
handler(int sig)
{
    crypt(str2, "xx");
    handled++;
}

int
main(int argc, char *argv[])
{
    char *cr1;
    int callNum, mismatch;
    struct sigaction sa;

    if (argc != 3)
        usageErr("%s str1 str2\n", argv[0]);

    str2 = argv[2];                      /* Делаем argv[2] доступным для обработчика */
    cr1 = strdup(crypt(argv[1], "xx"));  /* Копируем строку, возвращаемую crypt() в другой буфер */
    if (cr1 == NULL)
        errExit("strdup");

    sigemptyset(&sa.sa_mask);
    sa.sa_flags = 0;
    sa.sa_handler = handler;
    if (sigaction(SIGINT, &sa, NULL) == -1)
        errExit("sigaction");

    /* Многократно вызываем crypt() с использованием argv[1].
       Если прерывается обработчиком сигнала, то статическое хранилище,
       возвращаемое функцией crypt(), будет перезаписано результатом
       шифрования argv[2], и strcmp() обнаружит несоответствие с
       значением в 'cr1'. */

    for (callNum = 1, mismatch = 0; ; callNum++) {
        if (strcmp(crypt(argv[1], "xx"), cr1) != 0) {
            mismatch++;
            printf("Mismatch on call %d (mismatch=%d handled=%d)\n",
                    callNum, mismatch, handled);
        }
    }
}
/*
### Резюме:
Этот код демонстрирует проблему с ненадёжностью (или неатомарностью) некоторых функций библиотеки, таких как `crypt(3)`, когда они используются в многозадачных или многопроцессорных приложениях. В этом примере:

1. **Основная логика**:
   - Программа вызывает функцию `crypt` многократно в цикле, используя строку `argv[1]`. Обработчик сигнала, который перехватывает сигнал `SIGINT`, вызывает `crypt` с другой строкой, переданной через `argv[2]`.
   - Это приводит к тому, что статическая память, используемая функцией `crypt`, может быть перезаписана при каждом вызове обработчика сигнала. Если сигнал происходит в процессе работы программы, это может привести к несоответствию значений.

2. **Ненадёжность `crypt()`**:
   - Функция `crypt` использует статическое хранилище для хранения зашифрованной строки, что делает её неатомарной в многозадачных средах. В случае прерывания выполнения и вызова `crypt` внутри обработчика сигнала может произойти перезапись этой памяти, что вызовет неконсистентность.

3. **Основной цикл**:
   - Основной цикл программы многократно вызывает `crypt(argv[1], "xx")` и сравнивает результат с тем, что было сохранено в переменной `cr1`. Если значения не совпадают, это означает, что статическая память была перезаписана во время работы программы, и выводится сообщение о несоответствии.

4. **Результат**:
   - Если сигнал перехватывает выполнение программы в процессе работы с функцией `crypt()`, может быть выведено сообщение о "несоответствии". Это доказывает, что `crypt` не является безопасной для многозадачной среды функцией без дополнительной синхронизации или защиты.
*/