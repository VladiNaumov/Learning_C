/* bad_symlink.c

   Этот код демонстрирует ошибку при использовании symlink(): символическая
   ссылка создается с некорректным относительным путем, и последующий вызов
   chmod() завершается неудачей. Заметьте, что символические ссылки
   интерпретируются относительно каталога, в котором они находятся, а не
   текущего каталога процесса.
*/


#include <sys/stat.h>
#include <fcntl.h>
#include "tlpi_hdr.h"  // Включает заголовок с дополнительными функциями, такими как errExit()


int main(int argc, char *argv[]) {
    // Создаем директорию "test" с правами на чтение, запись и выполнение для пользователя
    if (mkdir("test", S_IRUSR | S_IWUSR | S_IXUSR) == -1)
        errExit("mkdir");
    
    // Переходим в директорию "test"
    if (chdir("test") == -1)
        errExit("chdir");

    // Создаем файл "myfile" с правами на чтение и запись для пользователя
    int fd = open("myfile", O_WRONLY | O_CREAT, S_IRUSR | S_IWUSR);
    if (fd == -1)
        errExit("open");
    if (close(fd) == -1)
        errExit("close");

    // Создаем символическую ссылку "../mylink", указывающую на "myfile" (ошибочный путь)
    if (symlink("myfile", "../mylink") == -1)
        errExit("symlink");

    // Пытаемся изменить права доступа на "../mylink" (это вызывает ошибку)
    if (chmod("../mylink", S_IRUSR) == -1)
        errExit("chmod");

    exit(EXIT_SUCCESS);
}

/*
Объяснение ошибки
В коде символическая ссылка создается с относительным путем "../mylink", указывающим на файл "myfile" внутри директории "test". 
Однако chmod() не может изменить права доступа для "../mylink", так как символические ссылки интерпретируются относительно каталога, 
где они находятся, а не текущего каталога процесса.
*/