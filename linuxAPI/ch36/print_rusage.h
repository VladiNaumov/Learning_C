#ifndef PRINT_RUSAGE_H      /* Предотвращение двойного включения */
#define PRINT_RUSAGE_H

#include <sys/resource.h>   /* Определяет структуру rusage */

/*
 * printRusage:
 * Форматированный вывод данных о системных ресурсах, использованных процессом.
 *
 * Параметры:
 * - leader: строка, добавляемая в начало каждой строки вывода.
 * - ru: указатель на структуру rusage с данными об использовании ресурсов.
 */
void printRusage(const char *leader, const struct rusage *ru);

#endif


