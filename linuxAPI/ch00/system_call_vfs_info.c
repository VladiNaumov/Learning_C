/*
список системных вызовов, которые используются для работы с виртуальной файловой системой (VFS) в Linux:

Основные системные вызовы для работы с VFS:
open — открытие файла.
close — закрытие файла.
read — чтение из файла.
write — запись в файл.
lseek — перемещение указателя в файле.
pread / pwrite — чтение и запись с указанием позиции.
truncate / ftruncate — изменение размера файла.
mmap — отображение файла в память.

Работы с атрибутами файлов:
stat / fstat / lstat — получение информации о файле.
chmod / fchmod — изменение прав доступа.
chown / fchown / lchown — изменение владельца.
access — проверка прав доступа.
utime / utimes / futimes — изменение временных меток.
Работы с каталогами:
mkdir — создание каталога.
rmdir — удаление каталога.
opendir / readdir / closedir — работа с каталогами.
chdir — смена текущего каталога.
getcwd — получение текущего каталога.

Ссылки:
link — создание жесткой ссылки.
unlink — удаление файла или жесткой ссылки.
symlink — создание символической ссылки.
readlink — чтение символической ссылки.
Работы с файловыми системами:
mount — монтирование файловой системы.
umount — размонтирование файловой системы.
statfs / fstatfs — получение информации о файловой системе.
sync — синхронизация с диском.

Работы с файловыми дескрипторами:
dup / dup2 — дублирование дескриптора.
fcntl — управление файловыми дескрипторами.
ioctl — выполнение операций с устройствами и файлами.

Прочее:
rename — переименование файла.
fallocate — выделение пространства для файла.
fsync / fdatasync — синхронизация изменений файла с диском.

Эти системные вызовы отвечают за основные операции, выполняемые с файлами, каталогами, ссылками и файловыми системами в Linux.

 */