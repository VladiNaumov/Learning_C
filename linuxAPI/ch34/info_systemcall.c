/* 34 Process Groups, Sessions, and Job Control */

#include <unistd.h>

/*
 * setsid - Создаёт новую сессию и делает вызывающий процесс её лидером.
 * Возвращает идентификатор сессии при успехе или -1 при ошибке.
 */
pid_t setsid(void);

#include <stdio.h>

/*
 * ctermid - Возвращает имя управляющего терминала текущего процесса.
 * ttyname - Указатель на буфер для имени терминала (может быть NULL).
 * Возвращает указатель на строку с именем терминала или NULL при ошибке.
 * Константа L_ctermid определяет минимальный размер буфера.
 */
char *ctermid(char *ttyname);

#include <unistd.h>

/*
 * tcgetpgrp - Получает идентификатор группы процессов, связанной с терминалом.
 * fd - Файловый дескриптор открытого терминала.
 * Возвращает идентификатор группы процессов при успехе или -1 при ошибке.
 */
pid_t tcgetpgrp(int fd);

/*
 * tcsetpgrp - Устанавливает группу процессов как владельца терминала.
 * fd - Файловый дескриптор открытого терминала.
 * pgid - Идентификатор группы процессов.
 * Возвращает 0 при успехе или -1 при ошибке.
 */
int tcsetpgrp(int fd, pid_t pgid);

/*
### Описание:
1. **`setsid`**: 
   - Применяется для отделения процесса от управляющего терминала.
   - Создаёт новую сессию, новый лидирующий процесс и группу процессов.

2. **`ctermid`**: 
   - Возвращает имя управляющего терминала текущего процесса.
   - Часто используется для получения информации о терминале, связанном с программой.

3. **`tcgetpgrp`** и **`tcsetpgrp`**:
   - Управляют группами процессов, связанными с терминалом.
   - Полезны для организации взаимодействия между терминалами и процессами, например, в программах-оболочках.
 */
