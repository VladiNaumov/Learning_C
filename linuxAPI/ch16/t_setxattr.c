/* t_setxattr.c

   Демонстрация использования setxattr() для установки расширенного атрибута файла.

   Эта программа специфична для Linux (2.6 и позднее).

   Также см. view_xattr.c.
*/
#include <sys/xattr.h>
#include "tlpi_hdr.h"

int
main(int argc, char *argv[])
{
    /* Проверка аргументов командной строки */
    if (argc < 2 || strcmp(argv[1], "--help") == 0)
        usageErr("%s file\n", argv[0]);  /* Сообщение о неправильном использовании */

    /* Установка значения первого атрибута */
    char *value = "The past is not dead.";
    if (setxattr(argv[1], "user.x", value, strlen(value), 0) == -1)
        errExit("setxattr");

    /* Установка значения второго атрибута */
    value = "In fact, it's not even past.";
    if (setxattr(argv[1], "user.y", value, strlen(value), 0) == -1)
        errExit("setxattr");

    exit(EXIT_SUCCESS);
}

/*

### Резюме:
Этот код демонстрирует использование функции `setxattr()` для установки расширенных атрибутов файла в Linux (версии 2.6 и выше). Программа устанавливает два расширенных атрибута для указанного файла. Первый атрибут называется `user.x` и получает строковое значение `"The past is not dead."`, а второй атрибут `user.y` получает значение `"In fact, it's not even past."`.

- **Основная цель**: Установка расширенных атрибутов для файла.
- **Основная функция**:
  - `setxattr`: используется для установки расширенных атрибутов для файла. Атрибуты могут быть полезны для хранения метаданных или для других целей, таких как безопасность.
  
- **Примечания**:
  - Программа проверяет, был ли передан путь к файлу в аргументах командной строки, и если аргументы указаны неверно, выводится сообщение о помощи.
  - В случае ошибки при установке атрибутов программа вызывает `errExit()`, что приводит к завершению работы с выводом ошибки.
  
*/