/* view_xattr.c

   Отображение расширенных атрибутов файла.

   Эта программа специфична для Linux (2.6 и позднее).

   Также см. t_setxattr.c.
*/
#include <sys/xattr.h>
#include "tlpi_hdr.h"

#define XATTR_SIZE 10000  /* Размер буфера для списка и значений атрибутов */

static void
usageError(char *progName)
{
    /* Вывод сообщения об ошибке использования */
    fprintf(stderr, "Usage: %s [-x] file...\n", progName);
    exit(EXIT_FAILURE);
}

int
main(int argc, char *argv[])
{
    char list[XATTR_SIZE], value[XATTR_SIZE];  /* Буферы для списка атрибутов и их значений */
    ssize_t listLen, valueLen;                   /* Длины списка атрибутов и значений */
    int ns, j, k, opt;
    Boolean hexDisplay;

    hexDisplay = 0;  /* По умолчанию не показывать в шестнадцатеричном формате */
    
    /* Обработка опций командной строки */
    while ((opt = getopt(argc, argv, "x")) != -1) {
        switch (opt) {
        case 'x': hexDisplay = 1;       break;  /* Включаем вывод в шестнадцатеричном формате */
        case '?': usageError(argv[0]);  /* Некорректный аргумент */
        }
    }

    if (optind >= argc)  /* Если не указаны файлы для обработки */
        usageError(argv[0]);

    /* Процесс обработки каждого файла, переданного в командной строке */
    for (j = optind; j < argc; j++) {
        /* Получение списка расширенных атрибутов файла */
        listLen = listxattr(argv[j], list, XATTR_SIZE);
        if (listLen == -1)
            errExit("listxattr");

        printf("%s:\n", argv[j]);

        /* Проход по всем именам атрибутов и отображение их значений */
        for (ns = 0; ns < listLen; ns += strlen(&list[ns]) + 1) {
            /* Вывод имени атрибута */
            printf("        name=%s; ", &list[ns]);

            /* Получение значения расширенного атрибута */
            valueLen = getxattr(argv[j], &list[ns], value, XATTR_SIZE);
            if (valueLen == -1) {
                printf("couldn't get value");  /* Ошибка при получении значения */
            } else if (!hexDisplay) {
                /* Если не требуется вывод в шестнадцатеричном формате */
                printf("value=%.*s", (int) valueLen, value);
            } else {
                /* Если выводить в шестнадцатеричном формате */
                printf("value=");
                for (k = 0; k < valueLen; k++)
                    printf("%02x ", (unsigned char) value[k]);
            }

            printf("\n");
        }

        printf("\n");
    }

    exit(EXIT_SUCCESS);
}

/*

### Резюме:
Этот код представляет собой программу для отображения расширенных атрибутов файла в Linux (2.6 и позже). Она позволяет просматривать имена и значения расширенных атрибутов для каждого файла, переданного в командной строке. Также предусмотрена возможность вывода значений атрибутов в шестнадцатеричном формате с помощью опции `-x`.

- **Основная цель**: Просмотр расширенных атрибутов файлов, таких как метаданные или списки контроля доступа (ACL).
- **Основные функции**:
  - `listxattr`: получает список всех расширенных атрибутов файла.
  - `getxattr`: извлекает значение конкретного атрибута.
- **Опции**:
  - Опция `-x` включает вывод значений атрибутов в шестнадцатеричном формате.
  
Программа полезна для работы с метаданными, хранимыми в расширенных атрибутах файлов, которые могут использоваться для разных целей, включая безопасность и управление доступом.
*/