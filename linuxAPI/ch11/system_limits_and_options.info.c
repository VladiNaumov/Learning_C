/* System Limits and Options  */


/*
 Каждая реализация UNIX накладывает ограничения (limits) на различные системные
характеристики и ресурсы и предоставляет возможности (options), определенные в раз-
личных стандартах (или отказывает в их предоставлении). Например, можно определить
следующее.
- Сколько файлов процесс может одновременно держать открытыми?
- Поддерживает ли система сигналы реального времени?
- Какое наибольшее значение может быть сохранено в переменной типа int?
- Насколько большим может быть список аргументов программы?
- Какова максимальная длина путевого имени?
*/

#include <unistd.h>

/**
 * Получает системный конфигурационный параметр.
 * @param name - параметр конфигурации (например, SC_CLK_TCK).
 * @return значение параметра или -1, если параметр не поддерживается.
 */
long sysconf(int name);

/**
 * Получает конфигурацию для указанного пути.
 * @param pathname - путь к файлу или директории.
 * @param name - параметр конфигурации (например, PC_NAME_MAX).
 * @return значение параметра или -1, если параметр не поддерживается.
 */
long pathconf(const char *pathname, int name);

/**
 * Получает конфигурацию для открытого дескриптора файла.
 * @param fd - дескриптор файла.
 * @param name - параметр конфигурации (например, PC_NAME_MAX).
 * @return значение параметра или -1, если параметр не поддерживается.
 */
long fpathconf(int fd, int name);

