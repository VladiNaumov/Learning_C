#include <stdio.h>

// Определяем тип указателя на функцию (коллбек)
typedef void (*Callback)(int result);

// Функция-коллбек, которая будет обрабатывать результат
void printResult(int result) {
    printf("Результат вычисления: %d\n", result);
}

// Функция умножения двух чисел, которая вызывает коллбек с результатом
void multiplyAndProcess(int a, int b, Callback callback) {
    int result = a * b; // Выполняем умножение
    callback(result);   // Вызываем коллбек с результатом
}

int main() {
    int x = 5;
    int y = 10;

    // Вызываем функцию multiplyAndProcess с нашими числами и коллбеком
    multiplyAndProcess(x, y, printResult);

    return 0;
}

/*
 * Пояснение:
    Определение коллбека:

    Мы объявляем тип Callback как указатель на функцию, которая принимает один параметр типа int и ничего не возвращает. Это наш коллбек.
    Функция printResult:

    Это конкретная функция-коллбек, которая будет вызываться с результатом вычисления. В данном случае она просто выводит результат на экран.
    Функция multiplyAndProcess:

    Эта функция принимает два числа a и b, выполняет их умножение и передает результат в коллбек.
    Использование коллбека в main:

    В функции main() мы передаем функцию printResult как коллбек в функцию multiplyAndProcess. После умножения чисел, результат будет передан в printResult, и результат вычисления будет выведен на экран.
 * */