/*
Типы квалификаторов в языке C позволяют управлять свойствами переменных и типов данных. Они модифицируют стандартные типы данных и указывают, как именно следует обращаться с данными. Основные квалификаторы типа в C включают const, volatile, static, и extern.
Каждый из этих квалификаторов имеет своё назначение и применяется для различных целей.

Основные квалификаторы типа
const

Назначение: Указывает, что значение переменной не должно изменяться после инициализации.
Это помогает предотвратить случайное изменение данных и позволяет компилятору оптимизировать код.
*/

const int MAX_SIZE = 100;
// В этом примере MAX_SIZE объявлен как константа, и его значение не может быть изменено после инициализации.

/*
volatile
Назначение: Указывает, что значение переменной может изменяться в любое время,
без какого-либо явного указания в программе (например, из аппаратного обеспечения или других потоков).
Это предотвращает оптимизации компилятора, которые могут предположить, что значение переменной не изменяется.
*/

volatile int hardware_status;
//В этом примере hardware_status может изменяться из аппаратного обеспечения, и компилятор не должен оптимизировать доступ к этой переменной.

/*
static
Назначение: Определяет область видимости переменной или функции.
При использовании внутри функции static переменная сохраняет своё значение между вызовами функции. При использовании на уровне глобального пространства имен static ограничивает видимость функции или переменной только текущим файлом.

Внутри функции:
*/

void increment() {
    static int counter = 0;
    counter++;
    printf("Counter: %d\n", counter);
}
//В этом примере counter сохраняет своё значение между вызовами функции increment.

//На уровне файла:

static int my_var = 42;
static void my_function() {
    // Функция доступна только в этом файле
}
//В этом примере my_var и my_function доступны только в текущем файле.

/*
extern
Назначение: Указывает, что переменная или функция объявлена в другом месте, возможно в другом файле.
Это позволяет использовать переменные и функции, определённые в других модулях программы.
*/

extern int global_variable;
extern void external_function(void);
//В этом примере global_variable и external_function определены в другом файле.


//Пример 1: const и volatile

#include <stdio.h>

void example() {
    const int MAX_LIMIT = 100;
    volatile int sensor_data = 0;

    // MAX_LIMIT не может быть изменён
    // sensor_data может изменяться из внешних источников
}

//Пример 2: static

#include <stdio.h>

static int global_counter = 0; // Доступен только в этом файле

void increment_counter() {
    static int local_counter = 0; // Сохраняет своё значение между вызовами
    local_counter++;
    global_counter++;
    printf("Local Counter: %d, Global Counter: %d\n", local_counter, global_counter);
}

// Пример 3: extern

// file1.c
#include <stdio.h>

int shared_variable = 10; // Определение переменной

void print_variable() {
    printf("Shared Variable: %d\n", shared_variable);
}

// file2.c
#include <stdio.h>

extern int shared_variable; // Объявление переменной

void change_variable() {
    shared_variable = 20;
}